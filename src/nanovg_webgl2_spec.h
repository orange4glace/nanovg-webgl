static napi_ref __nanovg_fetch_webgl2_func(const char* name) {
  napi_value func;
  napi_ref ref;
  napi_get_named_property(__tmp_env, __tmp_gl, name, &func);
  napi_create_reference(__tmp_env, func, 1, &ref);
  return ref;
}

